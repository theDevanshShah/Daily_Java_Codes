//Literals in JAVA
package com.First;

public class Literals {

public static void main(String[] args){

    byte bytee = 34;
    String  stringg = "R";
    float floatt = 1.34f;
    int intt = 134;
    double doublee = 1.34D;
    boolean booleann = true;
    boolean booleannn = false;
    short shortt = 134;
    long longg = 1344;



    System.out.println("Byte : " + bytee);
    System.out.println("String : " + stringg);
    System.out.println("Float : " + floatt);
    System.out.println("Integers : " + intt);
    System.out.println("Double : " + doublee);
    System.out.println("Boolean but True : " + booleann);
    System.out.println("Boolean but False : " + booleannn);
    System.out.println("Short : " + shortt);
    System.out.println("Long : " + longg);


}

}
